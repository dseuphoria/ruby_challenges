weather = 'rainy'
case weather
	when 'snowy'
		puts "Get your gloves and boots"
	when 'sunny'
		puts "Don't forget your sunscreen!"
	when 'rainy'
		puts "Don't leave the house without your umbrella today!"
	when 'windy'
		puts "You might want to bring your scarf today!"
	else
		puts "Dress in layers! who knows what will happen!"
	end
